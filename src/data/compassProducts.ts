// Helper function to filter products by various criteria
export function filterProducts(
  products: CompassProduct[],
  filters: {
    categories?: string[];
    tags?: string[];
    maxWeight?: number;
    maxPrice?: number;
    inStock?: boolean;
  }
): CompassProduct[] {
  return products.filter(product => {
    if (filters.categories && !filters.categories.includes(product.category)) {
      return false;
    }
    if (filters.tags && !filters.tags.some(tag => product.tags.includes(tag))) {
      return false;
    }
    if (filters.maxWeight && product.weight > filters.maxWeight) {
      return false;
    }
    if (filters.maxPrice && product.price > filters.maxPrice) {
      return false;
    }
    if (filters.inStock !== undefined && product.inStock !== filters.inStock) {
      return false;
    }
    return true;
  });
}

